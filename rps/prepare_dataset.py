"""
prepare_dataset.py
===================

Step 1 of the workflow: unpack the Roboflow dataset and make it usable by YOLO.

What this script does
---------------------
1. Unzips the downloaded dataset into the local ``dataset/`` folder.
2. Rewrites ``dataset/data.yaml`` so YOLO can find the images.

Why step 2 is necessary
------------------------
Roboflow's exported ``data.yaml`` points at the splits with paths like
``../train/images``. Those ".." paths are relative to wherever the file happens
to sit, and they break as soon as the dataset is moved. We replace them with a
single absolute ``path:`` root plus simple relative sub-paths, which is the
robust, recommended Ultralytics layout:

    path: /Users/.../yolo/dataset   # absolute root
    train: train/images
    val:   valid/images
    test:  test/images

Run it with:
    uv run prepare_dataset.py
    uv run prepare_dataset.py --force      # re-extract even if dataset/ exists
"""

from __future__ import annotations

import argparse
import zipfile

from config import CLASS_NAMES, DATASET_DIR, DATASET_ZIP, DATA_YAML


def count_images(split: str) -> int:
    """Count the .jpg images in one split folder (train / valid / test)."""
    images_dir = DATASET_DIR / split / "images"
    if not images_dir.is_dir():
        return 0
    # `glob` lists matching files; we wrap it in a list to count them.
    return len(list(images_dir.glob("*.jpg")))


def extract_dataset(force: bool) -> None:
    """Unzip the dataset archive into ``dataset/`` (skipping if already done)."""
    # If the dataset is already extracted, don't waste time doing it again,
    # unless the user explicitly asked us to with --force.
    if DATASET_DIR.exists() and not force:
        print(f"Dataset folder already exists at {DATASET_DIR} — skipping extraction.")
        print("(Pass --force to extract again from scratch.)")
        return

    if not DATASET_ZIP.exists():
        raise FileNotFoundError(
            f"Could not find the dataset zip at:\n  {DATASET_ZIP}\n"
            "Download it from Roboflow and place it there, or edit DATASET_ZIP in config.py."
        )

    print(f"Extracting {DATASET_ZIP.name} -> {DATASET_DIR} ...")
    DATASET_DIR.mkdir(parents=True, exist_ok=True)
    with zipfile.ZipFile(DATASET_ZIP) as archive:
        archive.extractall(DATASET_DIR)
    print("Extraction complete.")


def write_data_yaml() -> None:
    """Write a clean ``data.yaml`` with an absolute root path.

    We build the file as plain text on purpose: it keeps the result easy to read
    and means students can open ``dataset/data.yaml`` and immediately understand
    every line.
    """
    # `names` formatted the way YOLO expects, e.g. ['Paper', 'Rock', 'Scissors'].
    names_line = "[" + ", ".join(f"'{name}'" for name in CLASS_NAMES) + "]"

    content = f"""# Auto-generated by prepare_dataset.py — do not edit by hand.
# `path` is the absolute dataset root; the splits below are relative to it.
path: {DATASET_DIR}
train: train/images
val: valid/images
test: test/images

# nc = number of classes; names must be in class-id order (0, 1, 2).
nc: {len(CLASS_NAMES)}
names: {names_line}
"""
    DATA_YAML.write_text(content)
    print(f"Wrote dataset description -> {DATA_YAML}")


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__.splitlines()[1])
    parser.add_argument(
        "--force",
        action="store_true",
        help="Extract the zip again even if the dataset/ folder already exists.",
    )
    args = parser.parse_args()

    extract_dataset(force=args.force)
    write_data_yaml()

    # Show a quick summary so the student can confirm everything is in place.
    print("\nDataset summary")
    print("---------------")
    total = 0
    for split in ("train", "valid", "test"):
        n = count_images(split)
        total += n
        print(f"  {split:<6}: {n:>5} images")
    print(f"  {'total':<6}: {total:>5} images")
    print(f"\nClasses ({len(CLASS_NAMES)}): {', '.join(CLASS_NAMES)}")
    print("\nDone. Next step:  uv run train.py")


if __name__ == "__main__":
    main()
